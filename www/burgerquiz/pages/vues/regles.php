<div id="regles" class="container">
	<div class="titre"><h2>Règles du jeu solo</h2></div>
	<p>
		Une partie solo est composée de deux jeux de questions choisis aléatoirement dans 
		deux catégories différentes.<br>Pour chaque sous-question, vous avez 3 réponses possibles: l'un, l'autre, ou les deux.
		<br>
		<br>
		Une bonne réponse vous rapporte un point. Une mauvaise réponse ne vous rapporte pas de point.
	</p>
		<div class="titre"><h3>Calcul du score final: % de bonnes réponses - temps de réponse moyen.<h3></div>
	<p>
		Par exemple, si vous avez 85% de bonnes réponses, et que vous avez mis 5 secondes en moyenne 
		pour répondre aux questions, votre score sera de 80/100.
	</p>
	<div class="titre"><h2>Règles du jeu multijoueur<h2></div>
	<p>
		Une partie multijoueur se déroule de la même manière qu'une partie solo.<br>
		Le gagnant est le joueur ayant le plus de points.<br><br>
		Pour chaque sous-question, le joeur le plus rapide gagne le point, et les deux joeurs passent à la question suivante.
		<br>
		<br>
	</p>
</div>